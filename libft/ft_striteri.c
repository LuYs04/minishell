#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int	i;

	if (!s)
		return ((void) NULL);
	i = 0;
	while (*s)
	{
		f(i, s);
		s++;
		i++;
	}
}
